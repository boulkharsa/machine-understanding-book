// متغيرات التطبيق
let currentPage = 0;
let bookmarks = [0, 1, 3, 5, 9];
let fontSize = 1.1;

// تهيئة التطبيق
document.addEventListener('DOMContentLoaded', function () {
    initializeBook();
    setupEventListeners();
    updateProgress();
});

// تهيئة الكتاب
function initializeBook() {
    // استرجاع الصفحة المحفوظة
    const savedPage = localStorage.getItem('lastChapter');
    if (savedPage) {
        currentPage = parseInt(savedPage);
    }

    renderChapters();
    renderBookmarks();
    loadPage(currentPage);
    updateFontSize();
}

// عرض الفصول
function renderChapters() {
    const chaptersList = document.getElementById('chaptersList');
    chaptersList.innerHTML = '';

    bookData.forEach((chapter, index) => {
        const chapterItem = document.createElement('li');
        const chapterBtn = document.createElement('button');

        chapterBtn.className = `chapter-btn ${index === currentPage ? 'active' : ''}`;
        chapterBtn.setAttribute('data-chapter', index + 1);
        chapterBtn.textContent = chapter.title;

        chapterBtn.addEventListener('click', () => {
            currentPage = index;
            loadPage(currentPage);
            updateChaptersActive();
        });

        chapterItem.appendChild(chapterBtn);
        chaptersList.appendChild(chapterItem);
    });
}

// تحديث الفصول النشطة
function updateChaptersActive() {
    document.querySelectorAll('.chapter-btn').forEach((btn, index) => {
        if (index === currentPage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// تحميل الصفحة
function loadPage(pageIndex) {
    const page = bookData[pageIndex];
    const pageContent = document.getElementById('pageContent');
    const pageNumber = document.getElementById('pageNumber');

    pageContent.innerHTML = page.content;
    pageNumber.textContent = page.title;

    // تفعيل العناصر التفاعلية
    setTimeout(() => initInteractivity(bookData[pageIndex].id), 100);

    // حفظ الصفحة الحالية
    localStorage.setItem('lastChapter', currentPage);

    // تحديث تقدم القراءة
    updateProgress();

    // تحديث أزرار التنقل
    updateNavButtons();

    // التمرير إلى الأعلى
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

// تحديث أزرار التنقل
function updateNavButtons() {
    const prevBtn = document.getElementById('prevPage');
    const nextBtn = document.getElementById('nextPage');

    prevBtn.disabled = currentPage === 0;
    nextBtn.disabled = currentPage === bookData.length - 1;
}

// تحديث تقدم القراءة
function updateProgress() {
    const progress = Math.round(((currentPage + 1) / bookData.length) * 100);
    const progressFill = document.getElementById('progressFill');
    const progressPercentage = document.getElementById('progressPercentage');

    progressFill.style.width = `${progress}%`;
    progressPercentage.textContent = `${progress}%`;
}

// تحديث حجم الخط
function updateFontSize() {
    document.querySelector('.page-content').style.fontSize = `${fontSize}rem`;
}

// عرض العلامات المرجعية
function renderBookmarks() {
    const bookmarksList = document.getElementById('bookmarksList');
    bookmarksList.innerHTML = '';

    bookmarks.forEach(bookmarkId => {
        const chapter = bookData.find(ch => ch.id === bookmarkId);
        if (chapter) {
            const bookmarkItem = document.createElement('li');
            bookmarkItem.className = 'bookmark-item';
            bookmarkItem.textContent = chapter.title;
            bookmarkItem.addEventListener('click', () => {
                const chapterIndex = bookData.findIndex(ch => ch.id === bookmarkId);
                currentPage = chapterIndex;
                loadPage(currentPage);
                updateChaptersActive();
            });
            bookmarksList.appendChild(bookmarkItem);
        }
    });
}

// إعداد مستمعي الأحداث
function setupEventListeners() {
    // التنقل بين الصفحات
    document.getElementById('prevPage').addEventListener('click', () => {
        if (currentPage > 0) {
            currentPage--;
            loadPage(currentPage);
            updateChaptersActive();
        }
    });

    document.getElementById('nextPage').addEventListener('click', () => {
        if (currentPage < bookData.length - 1) {
            currentPage++;
            loadPage(currentPage);
            updateChaptersActive();
        }
    });

    // تغيير الوضع الليلي/النهاري
    document.getElementById('themeToggle').addEventListener('click', () => {
        document.body.classList.toggle('dark-mode');
        const icon = document.querySelector('#themeToggle i');
        const text = document.querySelector('#themeToggle');

        if (document.body.classList.contains('dark-mode')) {
            icon.className = 'fas fa-sun';
            text.innerHTML = '<i class="fas fa-sun"></i> وضع النهار';
        } else {
            icon.className = 'fas fa-moon';
            text.innerHTML = '<i class="fas fa-moon"></i> وضع الليل';
        }

        // حفظ التفضيل
        localStorage.setItem('darkMode', document.body.classList.contains('dark-mode'));
    });

    // التحكم في حجم الخط
    document.getElementById('increaseFont').addEventListener('click', () => {
        if (fontSize < 1.5) {
            fontSize += 0.1;
            updateFontSize();
            localStorage.setItem('fontSize', fontSize);
        }
    });

    document.getElementById('decreaseFont').addEventListener('click', () => {
        if (fontSize > 0.8) {
            fontSize -= 0.1;
            updateFontSize();
            localStorage.setItem('fontSize', fontSize);
        }
    });

    // إضافة علامة مرجعية بالنقر المزدوج على العنوان
    document.addEventListener('dblclick', function (e) {
        if (e.target.tagName === 'H2' || e.target.tagName === 'H3') {
            const chapterId = bookData[currentPage].id;
            if (!bookmarks.includes(chapterId)) {
                bookmarks.push(chapterId);
                renderBookmarks();

                // إظهار إشعار بسيط
                showNotification(`تمت إضافة "${bookData[currentPage].title}" إلى العلامات المرجعية`);
            }
        }
    });

    // التنقل السريع
    document.querySelectorAll('.quick-links a').forEach(link => {
        link.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href').substring(1);

            // البحث عن الفصل الذي يحتوي على العنوان المطلوب
            let targetPage = 0;
            bookData.forEach((chapter, index) => {
                if (chapter.content.includes(`id="${targetId}"`)) {
                    targetPage = index;
                }
            });

            currentPage = targetPage;
            loadPage(currentPage);
            updateChaptersActive();
        });
    });

    // تحميل التفضيلات المحفوظة
    if (localStorage.getItem('darkMode') === 'true') {
        document.body.classList.add('dark-mode');
        document.querySelector('#themeToggle i').className = 'fas fa-sun';
        document.querySelector('#themeToggle').innerHTML = '<i class="fas fa-sun"></i> وضع النهار';
    }

    if (localStorage.getItem('fontSize')) {
        fontSize = parseFloat(localStorage.getItem('fontSize'));
        updateFontSize();
    }

    // اختصارات لوحة المفاتيح
    document.addEventListener('keydown', function (e) {
        // سهم لليسار للصفحة التالية
        if (e.key === 'ArrowLeft' && currentPage < bookData.length - 1) {
            currentPage++;
            loadPage(currentPage);
            updateChaptersActive();
        }
        // سهم لليمين للصفحة السابقة
        if (e.key === 'ArrowRight' && currentPage > 0) {
            currentPage--;
            loadPage(currentPage);
            updateChaptersActive();
        }
        // زر D لتغيير الوضع
        if (e.key === 'd' || e.key === 'D') {
            document.getElementById('themeToggle').click();
        }
    });
}

// دالة لعرض الإشعارات
function showNotification(message) {
    const notification = document.createElement('div');
    notification.textContent = message;
    notification.style.cssText = `
                position: fixed;
                top: 20px;
                left: 20px;
                background: var(--secondary-color);
                color: white;
                padding: 10px 15px;
                border-radius: var(--border-radius);
                z-index: 10000;
                box-shadow: var(--shadow);
                animation: fadeInOut 3s ease;
            `;

    // إضافة أنيميشن
    const style = document.createElement('style');
    style.textContent = `
                @keyframes fadeInOut {
                    0% { opacity: 0; transform: translateY(-20px); }
                    10% { opacity: 1; transform: translateY(0); }
                    90% { opacity: 1; transform: translateY(0); }
                    100% { opacity: 0; transform: translateY(-20px); }
                }
            `;
    document.head.appendChild(style);

    document.body.appendChild(notification);
    setTimeout(() => {
        notification.remove();
        style.remove();
    }, 3000);
}

// التفاعل مع العناصر المضافة
function initInteractivity(chapterId) {
    // الفصل 1: الترانزيستور
    if (chapterId === 1) {
        const toggleBtn = document.getElementById('demo-toggle');
        const bulb = document.getElementById('demo-bulb');
        const status = document.getElementById('demo-status');
        let isOn = false;

        if (toggleBtn) {
            toggleBtn.addEventListener('click', () => {
                isOn = !isOn;
                if (isOn) {
                    bulb.classList.add('on');
                    status.innerHTML = '<span style="color:#fbbf24">شاعل (1)</span>';
                    toggleBtn.textContent = 'إطفاء';
                } else {
                    bulb.classList.remove('on');
                    status.innerHTML = 'طافي (0)';
                    toggleBtn.textContent = 'تشغيل';
                }
            });
        }
    }

    // الفصل 2: العداد الثنائي
    if (chapterId === 2) {
        const switches = document.querySelectorAll('.switch');
        const decimalValue = document.getElementById('decimal-value');

        function updateDecimal() {
            let total = 0;
            switches.forEach(sw => {
                if (sw.classList.contains('active')) {
                    total += parseInt(sw.getAttribute('data-value'));
                    sw.textContent = '1';
                } else {
                    sw.textContent = '0';
                }
            });
            decimalValue.textContent = total;
        }

        switches.forEach(sw => {
            sw.addEventListener('click', () => {
                sw.classList.toggle('active');
                updateDecimal();
            });
        });
    }

    // الفصل 3: ASCII
    if (chapterId === 3) {
        const input = document.getElementById('ascii-input');
        const codeDisplay = document.getElementById('ascii-code');
        const binaryDisplay = document.getElementById('ascii-binary');

        if (input) {
            input.addEventListener('input', (e) => {
                const char = e.target.value;
                if (char.length > 0) {
                    const code = char.charCodeAt(0);
                    codeDisplay.textContent = code;
                    binaryDisplay.textContent = code.toString(2).padStart(8, '0');
                } else {
                    codeDisplay.textContent = '0';
                    binaryDisplay.textContent = '00000000';
                }
            });
        }
    }

    // الفصل 6: البكسل
    if (chapterId === 6) {
        const pixels = document.querySelectorAll('.pixel');
        const colorBtns = document.querySelectorAll('.color-btn');
        let currentColor = 'red';

        colorBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                // إزالة التحديد من الجميع
                colorBtns.forEach(b => b.classList.remove('selected'));
                // تحديد الزر الحالي
                btn.classList.add('selected');
                currentColor = btn.getAttribute('data-color');
            });
        });

        pixels.forEach(pixel => {
            pixel.addEventListener('click', () => {
                pixel.style.backgroundColor = currentColor;
            });
        });
    }
}
